#include <iostream>
using namespace std;

int main() {
  int x = 5;
  int y = 10;
  int sum = x + y;
  int resta = x - y;
  int multiplicacion = x * y;
  int division = x / y;
  int modulo = x % y;
  cout << "Suma: " << sum << endl;
  cout << "Resta: " << resta << endl;
  cout << "Multiplicacion: " << multiplicacion << endl;
  cout << "Division: " << division << endl;
  cout << "Modulo: " << modulo << endl;
  return 0;
}
